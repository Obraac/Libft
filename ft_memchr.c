#include "libft.h"
#include <string.h>

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char	*ptr;
	size_t			i;

	ptr = (unsigned char *)s;
	i = 0;
	while (i < n && ptr[i] != c)
		i++;
	if (ptr[i] == c)
		return (&ptr[i]);
	return (NULL);
}
